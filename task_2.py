def num_sistem(a):                  # –§—É–Ω–∫—Ü–∏—è –∫–æ—Ç–æ—Ä–∞—è –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é
    b = len(a)                      # –ù–∞—Ö–æ–¥–∏–º –¥–ª–∏–Ω–Ω—É –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π
    s = list(iter(a))               # –ò–Ω—Ç–µ—Ä–∏—Ä—É–µ–º (–ø–µ—Ä–µ–¥–µ–ª—ã–≤–∞–µ–º —á–∏—Å–ª–æ –≤ —Å–ø–∏—Å–∫–æ–∫ –≥–¥–µ –∑–Ω–∞—á–µ–Ω–∏—è –∫–∞–∂–¥—ã–π —ç–ª–µ–º–µ–Ω—Ç —á–∏—Å–ª–∞)
    s.reverse()                     # –ü–µ—Ä–µ–≤–∞—Ä–∞—á–∏–≤–∞–µ–º —Å–ø–∏—Å–æ–∫ (–¥–ª—è –ø–µ—Ä–µ–≤–æ–¥–∞ –≤ 2 —Å–∏—Å—Ç–µ–º—É –∑–Ω–∞—á–µ–Ω–∏—è –±–µ—Ä—É—Ç—Å—è —Å –∫–æ–Ω—Ü–∞)
    num10 = 0                       # –î–æ–±–∞–≤–ª—è–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é –¥–ª—è –∑–∞–ø–∏—Å–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞
    for i in range(b):              # –í–µ–¥–µ—Ç –æ—Ç—á–µ—Ç –æ—Ç –Ω—É–ª—è –∏ –ø–æ–∫–∞ –Ω–µ –∑–∞–∫–æ–Ω—á–∏—Ç—Å—è —Å–ø–∏—Å–æ–∫ 'b'
        s[i] = int(s[i])            # –ü–µ—Ä–µ–≤–æ–¥–∏–º –≤ —á–∏—Å–ª–∞ –ø–æ—Å–∫–æ–ª—å–∫—É 'iter' –∑–∞–ø–∏—Å—ã–≤–∞–µ—Ç –∫–∞–∫ str
        s[i] = s[i] * 2 ** i        # –ö–∞–∂–¥—ã–π —Å–∏–º–≤–æ–ª —É–º–Ω–æ–∂–∞–µ–º –Ω–∞ 2 –≤ —Å—Ç–µ–ø–µ–Ω–∏ –∏–Ω–¥–µ–∫—Å(—Ç–∞–∫ –ø–µ—Ä–µ–≤–æ–¥–∏—Ç—Å—è –≤ 2-—É—é —Å–∏—Å—Ç–µ–º—É)
        num10 += s[i]               # –°–∫–ª–∞–¥—ã–≤–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
    return num10                    # –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç


a = input()                         # –í–≤–æ–¥–∏–º –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é
print(num_sistem(a))                # –í—ã–∑—ã–≤–∞–µ–º –∏ –≤—ã–≤–æ–¥–∏–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç —Ñ—É–Ω–∫—Ü–∏–∏



